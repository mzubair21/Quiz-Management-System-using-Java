    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        variable v=new variable();
        v.email=ef.getText();
        v.password=pf.getText();
        if(ef.getText().trim().isEmpty() || pf.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please Fill Both Field to Login");
        }else{
           DAO d = new DAO();
           v=d.authentication(v);
           if("1".equals(v.role_id)){
              //JOptionPane.showMessageDialog(null, "You will be redirected to Admin Panel"); 
              AdminPanel ap=new AdminPanel();
              this.dispose();
              ap.setVisible(true);
              ap.setvariable(v);
           }else if("2".equals(v.role_id)){
              //JOptionPane.showMessageDialog(null, "You will be redirected to Student Panel"); 
              this.dispose();
              v.email=ef.getText();
              v.password=pf.getText();
              v.member_id=d.getmember_id(v);
              if(d.checkResult(v)==0){
                selectCat sc = new selectCat(v);
              sc.setVisible(true);  
              }else{
                  //display result
              }
           }
           
        }
    }//GEN-LAST:event_jButton1ActionPerformed

